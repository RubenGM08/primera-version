package Unidad3;

public class EjemploForPar {
    public static void main(String[] args) {

        //Mostrar los numeros pares del 0 al 200
        for (int i=0; i<=200;i+=2){ // sintaxis es 1+=incremento que necesites
            System.out.println("El valor de i es -> " + i);
        }

        //Mostrar los numeros impares del 0 al 300
        for (int i=0; i<=300;i+=3){ // sintaxis es 1+=incremento que necesites
            System.out.println("El valor de i es -> " + i);
        }
    }
}
