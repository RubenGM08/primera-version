package Unidad3;

public class EjemploBucleAnidados {
    public static void main(String[] args) {

        System.out.println("***********Tabla de MULTIPLICAR ***********");
        for (byte i=0; i<=10; i++){
            for (byte f=0; f<=10; f++){
                System.out.println(i + " x " + f + " = " + (i*f));
            }
        }




    }
}
